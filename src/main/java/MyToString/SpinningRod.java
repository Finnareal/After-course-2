package MyToString;

public class SpinningRod {
    private String rodMaker;
    private double rodLength;
    private boolean isTelescopic;

    public SpinningRod(String rodMaker, double rodLength, boolean isTelescopic) {
        this.rodMaker = rodMaker;
        this.rodLength = rodLength;
        this.isTelescopic = isTelescopic;
    }

}
